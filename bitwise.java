// Bitwise Operators    & , | , ^ , ~
public class bitwise {
    public static void main(String args[]){
        int a=15,b=35;
        System.out.println("Bitwise AND : "+(a&b)); // 3
        System.out.println("Bitwise OR  : "+(a|b)); // 47
        System.out.println("Bitwise XOR : "+(a^b)); // 44
        System.out.println("Bitwise NOT : "+(~a));  // -16
    }
}
