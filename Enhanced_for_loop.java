// Enhanced for loop
public class Enhanced_for_loop {
    public static void main(String args[]) {
        int numbs[] = {13, 29, 33, 38, 55};
        for (int n : numbs) {
            System.out.println(n);
        }
    }
}
         /* Output: 13
                    29
                    33
                    38
                    55       */
